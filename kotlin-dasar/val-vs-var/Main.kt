fun main(){
    // penggunaan type inference (val), type data menyesuaikan dengan isi data
    // val nilai tidak bisa diganti
    val umur = 25
    println(umur)

    // penggunaan var
    // dapat di ubah isinya
    var nama = "andi"
    println(nama)

    nama = "andi pratama"
    println(nama)

}